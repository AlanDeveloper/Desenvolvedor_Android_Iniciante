public class Operacoes {
    public static void main(String[] args) {

        Integer numero = 10;

        System.out.println(numero + 960);
        System.out.println(numero - 89);
        System.out.println(numero * 2);
        System.out.println(numero / 3);
        System.out.println(numero % 2);

        numero *= 2;
        numero += 10;

        System.out.println(numero);

        numero++;
        numero--;
        numero--;

        System.out.println(numero);
    }
}
